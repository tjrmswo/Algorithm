#
# ********* 9 2*5-1
#  ******* 7 2*4-1
#   ***** 5 2*3-1
#    *** 3 2*2-1
#     * 1 2*1-1
#    *** 3 2*2-1
#   ***** 5 2*3-1
#  ******* 7 2*4-1
# ********* 9 2*5-1

N = int(input())
for i in range(N, 0, -1):
    print(" " * (N-i) + "*" * (2*i-1))
for j in range(2,N+1):
    print(" " * (N-j) + "*" * (2*j-1))
